#include "ft_ls.h"

void	doule_free(char **s1, char **s2)
{
	ft_strdel(s1);
	ft_strdel(s2);
}

int		doule_null(char **s1, char **s2)
{
	*s1 = NULL;
	*s2 = NULL;
	return (-1);
}
